function esc(s) { return String(s).replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;'); }

const params = new URLSearchParams(window.location.search);
const postId = parseInt(params.get('post'));

if (postId) {
  const post = POSTS.find(p => p.id === postId);
  document.getElementById('blogGrid').classList.add('hidden');
  const postEl = document.getElementById('blogPost');
  postEl.classList.remove('hidden');
  document.title = post ? `${post.title} — Multiple Mix` : 'Post Not Found';

  if (post) {
    postEl.innerHTML = `
      <div class="blog-post">
        <div class="blog-post__back" onclick="window.location.href='blog.html'">&#8592; Back to Blog</div>
        <img class="blog-post__image" src="${esc(post.image)}" alt="${esc(post.title)}" onerror="this.style.display='none'" />
        <div class="blog-post__meta">
          <span class="blog-card__cat">${esc(post.category)}</span>
          <span class="blog-card__date">${esc(post.date)}</span>
          <span class="blog-card__date">By ${esc(post.author)}</span>
        </div>
        <h1 class="blog-post__title">${esc(post.title)}</h1>
        <div class="blog-post__content">${post.content}</div>
      </div>`;
  } else {
    postEl.innerHTML = '<p style="padding:3rem;text-align:center;color:var(--text-gray)">Post not found. <a href="blog.html" style="color:var(--gold)">Back to blog</a></p>';
  }
} else {
  const grid = document.getElementById('blogGrid');
  POSTS.forEach(post => {
    const card = document.createElement('article');
    card.className = 'blog-card';
    card.onclick = () => window.location.href = `blog.html?post=${post.id}`;
    card.innerHTML = `
      <div class="blog-card__image">
        <img src="${esc(post.image)}" alt="${esc(post.title)}" onerror="this.style.display='none'" loading="lazy" />
      </div>
      <div class="blog-card__body">
        <div class="blog-card__meta">
          <span class="blog-card__cat">${esc(post.category)}</span>
          <span class="blog-card__date">${esc(post.date)}</span>
        </div>
        <h3 class="blog-card__title">${esc(post.title)}</h3>
        <p class="blog-card__excerpt">${esc(post.excerpt)}</p>
        <span class="blog-card__read">Read more &#8594;</span>
      </div>`;
    grid.appendChild(card);
  });
}
